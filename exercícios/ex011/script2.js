// Estrutura de repetição com variável de controle
for (var c = 0 ; c <= 10 ; c ++) { // Inicio (O que vc vai fazer antes da repeticao) / Condição / Incremento (O que vc vai fazer ao final da estrutura de repetição)
    console.log(`Passo ${c}`)
}

console.log('-- Fim da Repetição 1 --')
console.log('-- Início da Repetição 2 --')
